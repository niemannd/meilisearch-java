package io.github.niemannd.meilisearch.api.documents;

public class Type {
    private String name;

    private int number;

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return this.name;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public int getNumber() {
        return this.number;
    }
}
